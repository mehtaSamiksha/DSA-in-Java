//Narrowing Conversion/ Explicit Conversion/ Type Casting
import java.util.*;

public class typeCasting{
    public static void main(String args[]){
        Scanner sc = new Scanner(System.in);
        float a = 25.12f;
        int b = (int)a;
        System.out.println(b);

        char ch = 'A';
        int number = ch;
        System.out.println(number);
    }
}